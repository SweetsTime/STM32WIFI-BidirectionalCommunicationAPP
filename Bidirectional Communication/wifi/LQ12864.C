/*********************OLED驱动程序用的延时程序************************************/
void OLED_delay(unsigned int z)
{
  unsigned int x;
  for(x=z;x>0;x--)
    NOPS;
}

/**********************************************
//IIC Start
**********************************************/
void OLED_IIC_Start(void)
{
  OLED_SCL( high);		
  OLED_SDA (high);
  NOPS;
  OLED_SDA (low);
  OLED_SCL ( low);
  NOPS;
}

/**********************************************
//IIC Stop
**********************************************/
void OLED_IIC_Stop(void)
{
  OLED_SCL ( low);
  OLED_SDA ( low);
  NOPS;
  OLED_SCL( high);
  OLED_SDA ( high);
  NOPS;
}

/**********************************************
// IIC Write byte
**********************************************/
void Write_IIC_Byte(unsigned char IIC_Byte)
{
  unsigned char i;
  for(i=0;i<8;i++)
  {
    if(IIC_Byte & 0x80)
      OLED_SDA(high);
    else
      OLED_SDA(low);
    OLED_SCL( high);
    NOPS;
    OLED_SCL(low);
    IIC_Byte<<=1;
    NOPS;
  }
  OLED_SDA(high);
  OLED_SCL( high);
  NOPS;
  OLED_SCL(low);
  NOPS;
}

/*********************OLED写数据************************************/ 
void OLED_WrDat(unsigned char IIC_Data)	 
{
  OLED_IIC_Start();
  Write_IIC_Byte(0x78);
  Write_IIC_Byte(0x40);			//write data
  Write_IIC_Byte(IIC_Data);
  OLED_IIC_Stop();
}

/*********************OLED写命令************************************/
void OLED_WrCmd(unsigned char IIC_Command)
{
  OLED_IIC_Start();
  Write_IIC_Byte(0x78);            //Slave address,SA0=0
  Write_IIC_Byte(0x00);			//write command
  Write_IIC_Byte(IIC_Command);
  OLED_IIC_Stop();
}

/*********************OLED 设置坐标************************************/
void OLED_Set_Pos(unsigned char x, unsigned char y) 
{ 
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f));
} 

/*********************OLED全屏************************************/
void OLED_Fill(unsigned char bmp_dat) 
{
  unsigned char y,x;
  for(y=0;y<8;y++)
  {
    OLED_WrCmd(0xb0+y);
    OLED_WrCmd(0x01);
    OLED_WrCmd(0x10);
    for(x=0;x<X_WIDTH;x++)
      OLED_WrDat(bmp_dat);
  }
}

