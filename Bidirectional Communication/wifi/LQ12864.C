/*   
显示的时候，如果字符串长度超过16位 自动换行，最多64位
line： 行号       
str: 需要打印的字符串(注意：str 最多不能超过64位)
*/

//////////////////////////////

void OLED_Display(uint8_t line,char *str)
{
  uint8_t Line_Temp;
  uint8_t Str_Length=0,count,i;
  uint8_t Str_Error=0 ,Line_Error=0;
  
  
  switch (line)
  {
  case OLED_Line_1:
    Line_Temp = 0;
    break;
    
  case OLED_Line_2:
    Line_Temp = 2;
    break;
    
  case OLED_Line_3:  
    Line_Temp = 4;
    break;
    
  case OLED_Line_4:  
    Line_Temp = 6;
    break;
    
  default:
    
    OLED_P8x16StrLen(0,1, "Input Line Error",16);
    Line_Error=1;
    goto out;
    break;
    
  }
  
  Str_Length=strlen(str);
  
  if(Str_Length <= 16 && Str_Length > 0)
  {
    OLED_P8x16StrLen(0,Line_Temp, str, Str_Length);
  }
  
  else if(Str_Length >16 && Str_Length <= (16 * 4))
  {
    count=Str_Length/16;
    
    for(i=0;i<count;i++)
    {
      OLED_P8x16StrLen(0,Line_Temp, &str[i*16], 16);
      Line_Temp+=2;
    }
    
    OLED_P8x16StrLen(0,Line_Temp, &str[i*16],Str_Length%16 );
    
  }
  
  else
  {
    OLED_P8x16StrLen(0,2, "Input Str Error!",16);
    Str_Error = 1;
    goto out;
  }
  
out:
  if(Str_Error == 1)
    OLED_P8x16StrLen(0,4, "Check Str Length",16);
  
  if(Line_Error == 1)
    OLED_P8x16StrLen(0,4, "Check Line Num!",15);
  
  
}

void IIC_GPIO_Config(void)
{		
  /*定义一个GPIO_InitTypeDef类型的结构体*/
  GPIO_InitTypeDef GPIO_InitStructure;
  
  /*开启GPIOC的外设时钟*/
  RCC_APB2PeriphClockCmd( RCC_APB2Periph_GPIOB, ENABLE); 
  
  /*选择要控制的GPIOC引脚*/															   
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_6 | GPIO_Pin_7;	
  
  /*设置引脚模式为通用推挽输出*/
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;   
  
  /*设置引脚速率为50MHz */   
  GPIO_InitStructure.GPIO_Speed  = GPIO_Speed_50MHz; 
  
  /*调用库函数，初始化GPIOC*/
  GPIO_Init(GPIOB, &GPIO_InitStructure);		  
  
  /* 关闭所有led灯	*/
  GPIO_SetBits(GPIOB, GPIO_Pin_6 | GPIO_Pin_7);	 
}

void OLED_itoa(unsigned char x,unsigned char y,unsigned char *puls)
{   
  unsigned char i=0,j=0;
  unsigned char tan[2];
  
  
  tan[0]=*puls/16;
  tan[1]=*puls&0x0f;
  
  if(tan[0]<=0x09)
  {
    tan[0]=tan[0]+16;
  }
  else
  {
    tan[0]=tan[0]+23;
  }
  
  if(tan[1]<=0x09)
  {
    tan[1]=tan[1]+16;
  }
  else
  { 
    tan[1]=tan[1]+23;
  }
  
  if(x>120){x=0;y++;}
  
  OLED_Set_Pos(x,y);
  for(i=0;i<8;i++)
    OLED_WrDat(F8X16[tan[0]*16+i]);
  OLED_Set_Pos(x,y+1);
  for(i=0;i<8;i++)
    OLED_WrDat(F8X16[tan[0]*16+i+8]);
  x+=8;
  
  OLED_Set_Pos(x,y);
  for(i=0;i<8;i++)
    OLED_WrDat(F8X16[tan[1]*16+i]);
  OLED_Set_Pos(x,y+1);
  for(i=0;i<8;i++)
    OLED_WrDat(F8X16[tan[1]*16+i+8]);
  x+=8;
  j++;
  
}
